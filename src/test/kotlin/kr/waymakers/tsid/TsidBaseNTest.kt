class TsidBaseNTest {

	@Test
	fun testBase10() {
		val radix = 10
		val list = BASE10

		for (i in list.indices) {
			val o = BigInteger(list[i][0], 16)
			val tsid = Tsid(o.toLong())
			assertEquals(list[i][1], tsid.encode(radix))
			assertEquals(tsid, Tsid.decode(list[i][1], radix))
		}
	}

	@Test
	fun testBase43() {
